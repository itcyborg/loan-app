    {
        $this->validate($request,[
            'name'=>'required',
            'email'=>'required|unique:users,email|email'
        ]);
        $password=bin2hex(random_bytes(10));
        $data=[
            'email'=>request('email'),
            'name'=>request('name'),
            'password'=>Hash::make($password)
        ];
        return User::create($data);
    }

